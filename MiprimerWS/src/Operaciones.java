
public class Operaciones {

	private void sumar(int a, int b){
		int c;
		c= a + b;
		System.out.println("La suma de "+ String.valueOf(a) +" "+String.valueOf(b)+" es : "+ c);
	}
	
	private void restar(int a, int b){
		int c;
		c= a - b;
		System.out.println("La resta de "+ String.valueOf(a) +" "+String.valueOf(b)+" es : "+ c);
	}
	
}
