CREATE TABLE IF NOT EXISTS "CANALES" 
	("CHANNEL_ID_PK" CHAR(255) PRIMARY KEY  NOT NULL 
	,"TITULO" VARCHAR(255));

CREATE TABLE IF NOT EXISTS "TAGS" 
	("TAG_ID_PK" CHAR(255) PRIMARY KEY  NOT NULL );

CREATE TABLE IF NOT EXISTS "VIDEOS" 
	("VIDEO_ID_PK" CHAR(11) PRIMARY KEY  NOT NULL 
	,"CHANNEL_ID_FK" CHAR(255) 
	,"TITULO" VARCHAR(255));

CREATE TABLE IF NOT EXISTS "VIDEO_TAG_MAPS" 
	("VIDEO_ID_FK" CHAR(255) NOT NULL 
	,"TAG_ID_FK" CHAR(255) NOT NULL 
	,PRIMARY KEY ("VIDEO_ID_FK", "TAG_ID_FK"));
